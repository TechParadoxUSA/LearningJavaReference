import java.util.*;
public class LearningJava_SwitchStatement {
	
		public static void main(String[] args) 
			{
			int isThing;
			isThing = 1;
			
			switch (isThing){
			case 1:
				System.out.println("Thing is maybe a thing");
				break;
			case 2: 
				System.out.println("Thing is probs a thing");
				break;
			case 3: 
				System.out.println("Thing is could be thing");
				break;
			default:
				System.out.println("Thing is no-thing");
			break;

			
			
			}
			
			}
	
}
